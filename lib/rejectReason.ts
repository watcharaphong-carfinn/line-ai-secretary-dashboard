// จัดหมวด "เหตุผลที่ไม่อนุมัติ" — ต้องตรงกับ bot (src/leadParser.js rejectReasonOf) เป๊ะ
// เพื่อให้ฟิลเตอร์รายเคสในหน้างานเซลตรงกับตัวเลขในโดนัท (ซึ่ง bot คำนวณตอน sync)
const clean = (v: unknown) => String(v ?? "").replace(/\s+/g, " ").trim();

export const REJECT_REASONS = [
  "งานซ้ำ",
  "วงเงินไม่พอปิดหนี้",
  "ไม่เข้าเงื่อนไข",
  "ลูกค้าได้ที่อื่นแล้ว",
  "ติดต่อลูกค้าไม่ได้",
  "ลูกค้ายกเลิก/ไม่พร้อม",
  "อื่นๆ / ไม่ระบุ",
] as const;

export function rejectReasonOf(note: string, status: string): string {
  const t = `${clean(note)} ${clean(status)}`;
  if (/ซ้ำ/.test(t)) return "งานซ้ำ";
  if (/ทำสัญญา|ที่อื่น|เจ้าอื่น|ปิดที่อื่น|เงินติดล้อ|ได้รับว(ง)?เงินที่|ได้ยอดจัดที่/.test(t)) return "ลูกค้าได้ที่อื่นแล้ว";
  if (/วงเงินไม่พอ|วงเงินไม่ผ่าน|วงเงินต่ำ|ยอดไม่พอ|ไม่พอปิด|ยอดจัดไม่พอ|ยอดจัดได้ไม่พอ|ไม่เพียงพอ|ยอดต่ำ|ยอดจัดต่ำ|หนี้ท่วม|ยอดยังคงไม่/.test(t)) return "วงเงินไม่พอปิดหนี้";
  if (/ติดต่อ.*ไม่ได้|ติดต่อไม่ได้|ติดต่อไม่เกิน|ไม่รับสาย|โทรไม่ติด|โทรไม่รับ/.test(t)) return "ติดต่อลูกค้าไม่ได้";
  if (/ไม่เข้าเงื่อนไข|ไม่ผ่านเงื่อนไข|คุณสมบัติ|ประเมินไม่ผ่าน|รับเฉพาะ|ปีต่ำ|ไม่รับภาคใต้|ไม่รับรถ|เล่มชำรุด|ติดผ่อน|ล่าช้า|หลักเกณฑ|ไม่จัดยอด|เพิ่งซื้อ|ไม่ตรงเงื่อนไข/.test(t)) return "ไม่เข้าเงื่อนไข";
  if (/ยกเลิก|ไม่ทำ|ไม่สนใจ|เปลี่ยนใจ|ไม่พร้อม|ไม่โอน/.test(t)) return "ลูกค้ายกเลิก/ไม่พร้อม";
  return "อื่นๆ / ไม่ระบุ";
}
